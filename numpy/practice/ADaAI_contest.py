# Функция должна создавать массив из случайных целых чисел и находить среднее, медиану и стандартное отклонение. Созданный массив и результаты операций выведите с помощью return.
# Первый аргумент должен задавать размер массивов, второй и третий - нижнюю и верхнюю границу диапазона возможных значений соответственно.
def random_arrray3(shape, start, end):
    arr = np.random.randint(start, end, shape)
    arr_mean = np.mean(arr)
    arr_std = np.std(arr)
    arr_med = np.median(arr)
    return f"{arr} \n\n {arr_mean} \n\n {arr_std} \n\n {arr_med}"
